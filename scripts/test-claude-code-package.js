#!/usr/bin/env node

const fs = require('fs')
const os = require('os')
const path = require('path')
const childProcess = require('child_process')

const root = path.resolve(__dirname, '..')

function printHelp() {
  console.log(`Usage: node scripts/test-claude-code-package.js [options]

Create a local Claude Code smoke-test project and verify that Marketing Skills OS
can be discovered from Claude Code-style skill folders.

Options:
  --target <path>   Test project folder. Defaults to a temporary folder.
  --keep            Keep the generated test project.
  --quiet           Print only the final result.
  --help            Show this help message.

Examples:
  npm run test:claude-code
  npm run test:claude-code -- --keep
  npm run test:claude-code -- --target /private/tmp/marketing-skills-claude-code
`)
}

function parseArgs(argv) {
  const options = {
    target: '',
    keep: false,
    quiet: false,
    help: false,
  }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]
    if (arg === '--help' || arg === '-h') {
      options.help = true
    } else if (arg === '--target') {
      options.target = argv[index + 1]
      index += 1
    } else if (arg === '--keep') {
      options.keep = true
    } else if (arg === '--quiet') {
      options.quiet = true
    } else {
      throw new Error(`Unknown option: ${arg}`)
    }
  }

  return options
}

function log(options, message) {
  if (!options.quiet) console.log(message)
}

function listDirs(dir) {
  return fs.readdirSync(path.join(root, dir), { withFileTypes: true })
    .filter(entry => entry.isDirectory())
    .map(entry => entry.name)
    .sort()
}

function listFiles(dir, predicate = () => true) {
  return fs.readdirSync(path.join(root, dir), { withFileTypes: true })
    .filter(entry => entry.isFile() && predicate(entry.name))
    .map(entry => entry.name)
    .sort()
}

function runNode(script, args, options) {
  const command = [path.join(root, script), ...args]
  log(options, `run   node ${path.relative(root, command[0])} ${args.join(' ')}`.trim())
  return childProcess.execFileSync(process.execPath, command, {
    cwd: root,
    encoding: 'utf8',
    stdio: options.quiet ? 'pipe' : 'inherit',
  })
}

function copySkill(skill, destinationRoot) {
  const source = path.join(root, 'skills', skill)
  const destination = path.join(destinationRoot, skill)
  fs.mkdirSync(path.dirname(destination), { recursive: true })
  fs.cpSync(source, destination, { recursive: true, force: true })
}

function assertFile(file, message) {
  if (!fs.existsSync(file)) throw new Error(message || `Missing file: ${file}`)
}

function assertIncludes(file, value, message) {
  const text = fs.readFileSync(file, 'utf8')
  if (!text.includes(value)) throw new Error(message || `${file} is missing ${value}`)
}

function writeClaudePrompts(projectRoot, manifest, counts) {
  const workflowList = manifest.workflows
    .map(workflow => `- \`${workflow.id}\` -> ${workflow.name}`)
    .join('\n')

  const content = `# Claude Code Smoke Prompts

This folder was generated by \`npm run test:claude-code\`.

It simulates a project after installing Marketing Skills OS for Claude Code.

Compatibility note: the install path remains \`scayver/marketing-skills\`.

- Skills are available in \`.claude/skills/\`.
- Cross-agent skills are also mirrored in \`.agents/skills/\`.
- The Marketing OS workspace is available in \`.marketing-os/\`.
- The active workflow lives at \`.marketing-os/active-workflow.md\`.

## Inventory

| Asset | Count |
|-------|------:|
| Skills | ${counts.skills} |
| Integration guides in source repo | ${counts.integrations} |
| CLI tools in source repo | ${counts.clis} |
| Marketing Skills OS workflows | ${manifest.workflows.length} |

## Workflows Tested

${workflowList}

## Manual Claude Code Prompts

Run these prompts inside Claude Code from this test project folder.

### 1. Skill Discovery

\`\`\`text
Read .claude/skills/marketing-os/SKILL.md and tell me which Marketing OS workflow I should use for launching a SaaS product.
\`\`\`

Expected result: Claude Code references the \`marketing-os\` skill, reads the local skill file, and recommends the SaaS Launch workflow.

### 2. Product Context

\`\`\`text
Use product-marketing to create a positioning context for a privacy-first analytics tool for indie SaaS founders.
\`\`\`

Expected result: Claude Code follows the product-marketing skill structure and produces ICP, positioning, value proposition, messaging hierarchy, and competitive differentiation.

### 3. Workflow Execution

\`\`\`text
Use the marketing-os skill. Read .marketing-os/active-workflow.md, .marketing-os/product-context.md, .marketing-os/scoreboard.md, and .marketing-os/compliance-checklist.md. Produce the next concrete deliverable with one measurable metric.
\`\`\`

Expected result: Claude Code uses the active workflow and names the skill stack, integration guides, CLI helpers, deliverable, and metric.

### 4. Tool Reference Awareness

\`\`\`text
For the active workflow, identify the related integration guides and CLI helpers. Do not invent APIs. Tell me which local files you would read first.
\`\`\`

Expected result: Claude Code points back to local files from the Marketing OS workflow and avoids connector-specific assumptions.
\n`

  fs.writeFileSync(path.join(projectRoot, 'CLAUDE_CODE_SMOKE_PROMPTS.md'), content)
}

function main() {
  let options
  try {
    options = parseArgs(process.argv.slice(2))
  } catch (error) {
    console.error(error.message)
    printHelp()
    process.exit(1)
  }

  if (options.help) {
    printHelp()
    return
  }

  const projectRoot = options.target
    ? path.resolve(options.target)
    : fs.mkdtempSync(path.join(os.tmpdir(), 'marketing-skills-claude-code-'))

  const createdTemp = !options.target
  const skillDirs = listDirs('skills')
  const integrationFiles = listFiles('tools/integrations', name => name.endsWith('.md') && name !== 'bit-integrations-registry.md')
  const cliFiles = listFiles('tools/clis', name => name.endsWith('.js'))
  const manifest = JSON.parse(fs.readFileSync(path.join(root, 'docs', 'MARKETING_OS_MANIFEST.json'), 'utf8'))

  try {
    fs.mkdirSync(projectRoot, { recursive: true })
    log(options, `Claude Code smoke project: ${projectRoot}`)

    runNode('scripts/generate-marketing-os-dashboard.js', ['--check'], options)
    runNode('scripts/generate-marketing-os-index.js', ['--check'], options)

    for (const destination of ['.claude/skills', '.agents/skills']) {
      const destinationRoot = path.join(projectRoot, destination)
      fs.rmSync(destinationRoot, { recursive: true, force: true })
      for (const skill of skillDirs) copySkill(skill, destinationRoot)
      assertFile(path.join(destinationRoot, 'marketing-os', 'SKILL.md'), `${destination}/marketing-os/SKILL.md was not installed`)
      assertFile(path.join(destinationRoot, 'product-marketing', 'SKILL.md'), `${destination}/product-marketing/SKILL.md was not installed`)
      assertFile(path.join(destinationRoot, 'prospect-research', 'SKILL.md'), `${destination}/prospect-research/SKILL.md was not installed`)
    }

    runNode('scripts/init-marketing-os.js', ['--target', projectRoot, '--force', '--quiet'], options)

    const outputRoot = path.join(projectRoot, '.marketing-os')
    assertFile(path.join(outputRoot, 'README.md'), 'Marketing OS workspace README was not created')
    assertFile(path.join(outputRoot, 'marketing-os-manifest.json'), 'Marketing OS manifest was not created')

    for (const workflow of manifest.workflows || []) {
      runNode('scripts/run-marketing-os-workflow.js', ['--target', projectRoot, '--workflow', workflow.id, '--force', '--quiet'], options)
      assertIncludes(
        path.join(outputRoot, 'active-workflow.md'),
        workflow.name,
        `Active workflow did not include ${workflow.name}`,
      )
      assertIncludes(
        path.join(outputRoot, 'active-workflow.md'),
        'Product Context Readiness Gate',
        `Active workflow for ${workflow.name} did not include the product context readiness gate`,
      )
      assertIncludes(
        path.join(outputRoot, 'active-workflow.md'),
        'If `product-context.md` is still a blank template, pause workflow execution and run `product-marketing` first.',
        `Active workflow for ${workflow.name} did not route blank context to product-marketing`,
      )
    }

    runNode('scripts/review-marketing-os-week.js', ['--target', projectRoot, '--date', '2026-05-28', '--force', '--quiet'], options)
    runNode('scripts/snapshot-marketing-os.js', ['--target', projectRoot, '--date', '2026-05-28', '--force', '--quiet'], options)
    const status = runNode('scripts/status-marketing-os.js', ['--target', projectRoot, '--quiet'], options) || ''

    if (options.quiet && !status.includes('ready:')) {
      throw new Error('Marketing OS status did not report a ready workspace')
    }

    assertFile(path.join(outputRoot, 'current-weekly-review.md'), 'Weekly review was not created')
    assertFile(path.join(outputRoot, 'state', 'current-state.json'), 'State snapshot JSON was not created')
    assertFile(path.join(outputRoot, 'state', 'current-state.md'), 'State snapshot Markdown was not created')

    writeClaudePrompts(projectRoot, manifest, {
      skills: skillDirs.length,
      integrations: integrationFiles.length,
      clis: cliFiles.length,
    })

    console.log(`Claude Code smoke test passed: ${skillDirs.length} skills installed, ${manifest.workflows.length} workflows exercised.`)
    console.log(`Test project: ${projectRoot}`)
    console.log(`Manual prompts: ${path.join(projectRoot, 'CLAUDE_CODE_SMOKE_PROMPTS.md')}`)
  } finally {
    if (createdTemp && !options.keep) {
      fs.rmSync(projectRoot, { recursive: true, force: true })
    }
  }
}

try {
  main()
} catch (error) {
  console.error(`Claude Code smoke test failed: ${error.message}`)
  process.exit(1)
}
